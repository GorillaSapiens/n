sub pow2s {
   my ($b) = @_;
   my $x = Math::BigInt->new(1);
   $x->blsft($b);
   return $x->bstr();
}

sub all1s {
   my ($b) = @_;
   my $x = Math::BigInt->new(1);
   $x->blsft($b);
   $x->bdec();
   return $x->bstr();
}

sub typed {
   my ($v, $t) = @_;
   return "$v`$t";
}

sub biass {
   my ($b) = @_;
   my $x = Math::BigInt->new(1);
   $x->blsft($b - 1);
   $x->bdec();
   return $x->bstr();
}

