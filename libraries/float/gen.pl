sub mag_cmp_snippet {
   my ($dst, $lhs, $rhs, $indent) = @_;
   $indent //= '   ';
   my $s = "${indent}$dst := $CMP_EQ;\n";
   for (my $i = $last; $i >= 0; --$i) {
      my $l = "($lhs.bytes[$i] & 255)";
      my $r = "($rhs.bytes[$i] & 255)";
      if ($i == $last) {
         $l = "($l & 127)";
         $r = "($r & 127)";
      }
      $s .= "${indent}if ($dst == $CMP_EQ) {\n";
      $s .= "${indent}   if ($l < $r) { $dst := $CMP_LT; }\n";
      $s .= "${indent}   else { if ($l > $r) { $dst := $CMP_GT; } }\n";
      $s .= "${indent}}\n";
   }
   return $s;
}

sub pack_snippet {
   my ($sig, $exp, $sign, $out, $mant) = @_;
   return "      $out.raw := $ZERO;
"
      . "      if ($sig == $M_ZERO) {
"
      . "         $out.bits.sign := $sign;
"
      . "      }
"
      . "      else {
"
      . "         ${matht} low;
"
      . "         low := $sig & 7`$matht;
"
      . "         $mant := $sig / $M_EIGHT;
"
      . "         if ((low & 4`$matht) != $M_ZERO && (((low & 3`$matht) != $M_ZERO) || (($mant & $M_ONE) != $M_ZERO))) {
"
      . "            $mant++;
"
      . "         }
"
      . "         if ($mant >= $M_CARRY) {
"
      . "            $mant /= $M_TWO;
"
      . "            $exp++;
"
      . "         }
"
      . "         if ($exp >= $M_EXP_MAX) {
"
      . "            $out.bits.sign := $sign;
"
      . "            $out.bits.exponent := $EXP_MAX;
"
      . "            $out.bits.mantissa := $ZERO;
"
      . "         }
"
      . "         else {
"
      . "            $out.bits.sign := $sign;
"
      . "            if ($mant >= $M_HIDDEN) {
"
      . "               $out.bits.exponent := $exp;
"
      . "               $out.bits.mantissa := $mant & $FRACMASK;
"
      . "            }
"
      . "            else {
"
      . "               $out.bits.exponent := $ZERO;
"
      . "               $out.bits.mantissa := $mant;
"
      . "            }
"
      . "         }
"
      . "      }
";
}


